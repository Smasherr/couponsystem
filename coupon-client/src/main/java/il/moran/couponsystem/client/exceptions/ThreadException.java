package il.moran.couponsystem.client.exceptions;



/*
 * ThreadException throws exceptions for Threads
 */
public class ThreadException extends Exception {



	
	private static final long serialVersionUID = 2958722713174105868L;

	public ThreadException() {
		super();
	}
	
	public ThreadException(String msg) {
		super(msg);
	}
	
	
}
